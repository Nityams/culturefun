-- Clear current deck and build new one with desired difficulty
function buildDeck(difficulty)
  -- Discard
  deck = {}

  -- Build
  -- Easy
  if difficulty > 0 then
    print("Adding easy countries")
    for k,v in pairs(countries_easy) do table.insert(deck, v) end
  end
  -- Medium
  if difficulty > 1 then
    print("Adding medium countries")
    for k,v in pairs(countries_medium) do table.insert(deck, v) end
  end
  -- Hard
  if difficulty > 2 then
    print("Adding hard countries")
    for k,v in pairs(countries_hard) do table.insert(deck, v) end
  end
end

-- Randomizes deck order
function shuffleDeck()
  -- Ok I get this
  math.randomseed(os.time())
  local rand = math.random

  -- What is going on
  local iterations = #deck
  local j

  -- Black magic I say
  for i = iterations, 2, -1 do
    j = rand(i)
    deck[i], deck[j] = deck[j], deck[i]
  end
end

-- Remove a number of countries from the deck
function drawDeck(count)
  draw = {}
  for i=1,count do
    -- Removing table element shifts all elements, so index 1
    draw[i] = deck[1]
    table.remove(deck, 1)
  end
  return draw
end

-- Number of cards left in the deck
function leftInDeck()
  count = 0
  for i,v in ipairs(deck) do
    count = count + 1
  end
  return count
end

